CREATE TABLE TUSERS(
id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1),
email VARCHAR(255),
isadmin BOOLEAN,
login varchar(255),
status VARCHAR(255),
password VARCHAR(255),
CONSTRAINT pk_users PRIMARY KEY (id));

CREATE TABLE TCATEGORIES(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1),
    name VARCHAR(255),
    user_id BIGINT,
    CONSTRAINT pk_categories PRIMARY KEY(id),
    CONSTRAINT fk_users_cat FOREIGN KEY(user_id) REFERENCES TUSERS(id));

CREATE TABLE TTASKS(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1),
    title VARCHAR(255),
    comments VARCHAR(255),
    created DATE,
    planned DATE,
    finished BOOLEAN,
    user_id BIGINT,
    category_id BIGINT,
    CONSTRAINT pk_tasks PRIMARY KEY(id),
    CONSTRAINT fk_users_tasks FOREIGN KEY(user_id) REFERENCES TUSERS(id),
    CONSTRAINT fk_categories_tasks FOREIGN KEY(category_id) REFERENCES TCATEGORIES(id));
